    "single-board computers like Orange Pi into AI agent hosts. It runs LLMs locally, "
    "supports RAG memory, manages containers, and provides a web dashboard. "
    "Discuss TinyAgentOS from your unique perspective. Keep responses to 2-3 sentences. "
    "Be conversational and respond to what others have said when relevant."
